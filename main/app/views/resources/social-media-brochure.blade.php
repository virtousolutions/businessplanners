<h2>WHAT IS SOCIAL MEDIA?</h2>

<p>Social media is a means of communication and marketing, made possible by the use of tools on the web that lets people share things like pictures, web pages and movies. Social media also allows people to interact with these digital items in a variety of
ways, such as voting, commenting or reviewing.</p>

<p>In really simple terms, social media & social networking are about communication.</p>

<p>Chances are that you’ve seen or heard of
social media, maybe by another name.
How many of you have heard of Facebook?
Well that’s social media, albeit just one
example.</p>

<p>Facebook is a massive global service,
which has its biggest audiences in the US
and Britain.</p>

<ul>
	<li>Facebook has over 900 + Million members</li>

	<li>More than half of those are aged
	18 - 24 years</li>

	<li>Users between 35 - 54 years of age logging
	in the first half of 2009 grew 276%</li>

	<li>Users in that age bracket doubles every
	2 months!</li>

	<li>This will be the first year social networking
	sites will be used in the elections!</li>

	<li>AND NOW Facebook is even becoming
	prime source for divorce case evidence!!</li>

</ul>

<p>Facebook is an interesting example, because
it’s both a venue for social media, and it’s a
social network, which essentially means it’s a
place for people to connect on the web.</p>

<p>For the most part, social media is about
sharing things (like music, pictures, video
and web pages) with friends, family and work
colleagues, and even customers.</p>

<p>And because this is about being “social”, you’ll
have a profile on a website like Facebook,
telling people a little about you, what you do
and where you live. Social media is also a good
way to promote ideas, projects, upcoming
events etc.</p>

<p>Staying with Facebook for a moment,
there are things like Groups and Pages;
where people get together and talk about a
particular interest. But a lot of people post
photos, or send messages to each other, some
of which are visible on their own Profile pages.</p>

<p>Also, because there are things like Groups
and Pages, people as well as businesses can
create a presence for themselves, around an
idea, an interest, a product, a service or even a
brand, which they get to share with everybody.</p>


