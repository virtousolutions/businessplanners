<h2>Why Write a Business Plan?</h2>

<p>A Business Plan helps you evaluate the feasibility of a new business idea in an objective, critical, and unemotional way.</p>

<ul>
	<li>Marketing – Is there a market? How much can you sell?</li>
	<li>Management – Does the management team have the skill?</li>
	<li>Financial – Can the business make a profit?</li>
</ul>

<p>It provides an <strong>operating plan</strong> to assist you in running the business and improves your probability of success.</p>

<ul>
	<li>Identify opportunities and avoid mistakes</li>
	<li>Develop production, administrative, and marketing plans</li>
	<li>Create budgets and projections to show financial outcomes</li>
</ul>

<p>It <strong>communicates your idea</strong> to others, serves as a “selling tool,” and provides the basis for your financing proposal.</p>

<ul>
	<li>Determine the amount and type of financing needed</li>
	<li>Forecast profitability and investor return on investment</li>
	<li>Forecast cash flow, show liquidity and ability to repay debt</li>
</ul>

<p><strong>Who will use the plan?</strong> If you won't use the plan to raise money, your plan will be internal and may be less formal. If you are presenting it to outsiders as a financing proposal, presentation quality and thorough financial analysis are very important.</p>


